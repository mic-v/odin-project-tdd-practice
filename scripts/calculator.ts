export let calculator = {
    add: function(a: number, b: number) {
        return a + b;
    },
    subtract: function(a: number, b: number) {
        return a - b;
    },
    divide: function(a: number, b: number) {
        return a / b;
    },
    multiply: function(a:number, b: number) {
        return a * b;
    }
};

